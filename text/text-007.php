A eso de las 4:00 am sonó el primer estallido.
Un ruido fuerte como un petardo subió toda la verde cordillera hasta llegar a la casa de E,
quien tenía el sueño más frágil de la tierra.
<br>
<br>
Le faltaban todavía treinta minutos de sueño esa calurosa noche de
Agosto para completar sus sagradas siete horas de reposo y pocas
cosas le molestaban más que lo despertaran a destiempo.
<br>
<br>

 “¿Qué fue eso? - Preguntó su somnolienta mujer,
 en un murmullo ininteligible.
 <br><br>

 “Seguramente fueron los animales" -respondió E dentro del cansancio-. <br><br>

"Hay una vaca enferma. Ayer se lastimó una pata bajando la colina.
Tendré que llamar al veterinario, maldita sea. Esa vaca cuando se enferma
procura hacerme la vida imposible-.
<br><br>

Se escuchó una segunda explosión. Esta vez un poco más alejada que la
primera mas nítidamente audible.
<br><br>
 “Seguramente los del pueblo están de fiesta -contestó E desde los
 países de la pereza, anticipándose a la previsible pregunta de su mujer-.
 La gente busca cualquier excusa para festejar y mañana probablemente
 será el día de algún nuevo santo de infinita devoción" -
 y se sumergió entre las sábanas.
 <br><br>

Con la tercera explosión se le notificó al señor E lo poco
satisfactorias que habían sido sus respuestas y se vió obligado
a levantarse para corroborar de dónde venían esos ruidos.
<br><br>

Era E un hombre bueno y tranquilo, quien vivía en una hermosa
casa en lo alto de una montaña con su esposa y sus dos hijas.
<br><br>

Su casa tenía una puerta doble grande de madera, ventanas de marco rojo,
y las paredes pastel sostenían un elaborado tejado de roble que les protegía
de la lluvia. Para esos días en los que el cielo lloraba y E se permitía
la locura de soñar con una vida vertiginosa y desordenada, impredecible,
llena de coloridos personajes en lugares salvajes.<br><br>

Se levantó de la cama con gran esfuerzo, pues aunque no era obeso,
tenía una panza enorme, como si se hubiera tragado una res entera.
Se limpió los ojos y se dio un minuto de paz para entregarse al dolor
de que ya no iba a poder volver a dormir.<br><br>

"No dormir bien es un desastre -pensaba para sí mismo mientras
se calzaba sus botas de cuero-. Todo el trabajo que hay que hacer
para dormir bien nunca es bien valorado-.
<br>
<br>

Salió por la puerta principal de la casa, ante la cual descendía
elegantemente una enorme cordillera de color verde intenso,
en donde pasteaban sus vacas. El alba no había llegado,
el campo estaba sumergido en la profunda penumbra.
<br>
<br>
E afinó su ojo para acostumbrarlo a la oscuridad.
<br><br>
Justo cuando sus ojos hubieron absorbido la poca luz del firmamento, un fugaz relámpago
lejano iluminó la cordillera, tras lo cual el violento impacto de una onda expansiva le arrojó al suelo.
<br><br>
Una columna de humo ascendía desde el medio del valle.
<br><br>
Al recomponerse del golpe, un tanto aturdido, volteó la cabeza hacia la puerta
de su casa, desde donde su mujer lo miraba con cara de preocupación. Sin tener que decirse más,
E salió corriendo hacia el establo de caballos que se encontraba a unos cien metros de la casa, justo donde reunía su ganado cuando tenía qué ordenar o vacunarles. Ensilló a su caballo favorito, un táparo veloz, resistente y muy noble que le había servido desde que era un potrillo arisco.
<br><br>
Lo azotó como en su juventud y el animal, siempre fiel y decidido, no dudó un segundo en arrancar con un brinco y dar todo lo que sus patas podían dar.<br><br>

El corazón de E dio otro salto, en un diástole inesperado como el sonido que lo despertara, haciendo correr la sangre por sus venas y desatar la adrenalina cuando sintió el ímpetu de su caballo. La perspectiva de que algo malo hubiera pasado en el pueblo y que alguien hubiera salido herido le hicieron empezar a sudar.<br><br>

El caballo Jacinto  “volaba- cuesta abajo y cuando digo que  “volaba ¡- quiero decir que este caballo de setenta y dos meses de edad, quinientos ocho kilogramos de peso y ciento sesenta centímetros de altura se desplazaba a una velocidad de setenta y cuatro kilómetros por hora sobre una pendiente del catorce por ciento.
<br><br>
Para algún tercero habría sido un espectáculo ver correr al animal esa calurosa noche de Agosto, saltando obstáculos, zigzagueando por el potrero y siguiendo con finura la tenue línea de trocha que unía los terrenos de E con Nueva Cajebaera, el pueblo de donde provenían las explosiones. Era como ver caer una lluvia de elefantes africanos adultos en el mar.
<br><br>
Sus patas apenas golpeaban el suelo, con fiereza, arrancando grandes trozos de hierba y el viento hacía zumbar las orejas de E, cuyos lagrimeantes ojos distinguieron, ya en las proximidades del caserío oculto en medio del valle, una voraz llamarada que se propagaba hambrienta entre las casas aledañas a la plaza principal.
<br><br>
Jacinto aumentó el ritmo, sin metrónomo, de su violento galope mientras E bregaba no perder las riendas y mantenerse en la silla, llegando en cortísimo tiempo a la plaza de la iglesia y constatar, no sin horror repulsivo, tres cuerpos de personas en el suelo y una figura inclinada sobre ellos.<br><br>

"  ¿Qué ha pasado?"-gritó ansioso E, haciendo un esfuerzo por no permitir que su voz quebrase -.<br><br>

La figura inclinada se enderezó asustada, y E distinguió la angustiada cara de Don Antonio, un vecino recién jubilado cuyos nublados ojos, por primera vez desde hacía más de veinte años, reflejaban terror y dolor.
<br><br>
- "No lo sé. Estaba en el bosque y he oído tres explosiones. He venido tan rápido como he podido.-- Apartó la mirada, abochornado, y la dirigó al suelo -.- No he encontrado a nadie..."-. Don Antonio sudaba y tenía una expresión que, vista en un niño de diez años parece graciosa pero que vista en un experimentado hombre de sesenta-y-cinco años ya no causa tanta gracia.
<br><br>
- "¿Dónde está la gente? - Gritó E.- Jacinto relinchaba nervioso por el sonido de la madera quemándose.
<br><br>
- "  ¡Le he dicho que no lo sé, vaya y busque ayuda! Yo estoy bien, no se preocupen por mí, ellos no tanto"-.  Respondió Don Antonio señalando los cuerpos de tres muchachos en el suelo.
<br><br>
E se bajó del caballo, y se inclinó para ayudar al primer muchacho. Miró a Don Antonio y éste negó con la cabeza. Con el dolor de la impotencia miró a Don Antonio, quien le señaló al tercer muchacho acostado: Su respiración era muy tenue. Estaba vivo. El sobreviviente tenía una profunda herida en el pecho por un pedazo de madera, en la plaza cerca había una fuente, tomó fresco líquido de la vida y lo aplicó en la herida, diole de beber a la criatura y en un escupitajo de sangre reaccionó y despertó. Era un milagro que siguiera vivo.
<br><br>
Disparos escucháronse a lo lejos y E, instruyendo a Don Antonio que se refugiase en la casa más cercana con el muchacho tendido en el suelo, se dirigió con sigilo de gato al encuentro de los propietarios de los disparos, de quienes quedaba por constatar si eran amigos, o enemigos.
<br><br>
Detrás del fuego que consumía una de las casas, un pequeño escuadrón de cinco hombres avanzaba por la calle con fusiles preparados, en movimientos coreografiados, cubiertos con vestimenta camuflada.
<br><br>
Su primera reacción fue de alivio y alegría, al pensar que eran hombres del gobierno, probablemente militares que habían venido a ayudar. Sin embargo, una corazonada de esas que desarrolla la gente con los años le impidió reaccionar al impulso de pedir ayuda.   ¿Qué tal si estos militares fueron los que iniciaron el fuego?   ¿Qué buscaban en este pueblo que no era una amenaza para nadie?
<br><br>
Al E se le congelaron las ideas. Su mente se quedó en blanco y palideció de pavor.
