Bazi lo miraba atentamente. Lo que el ojo vulgar vería, con la natural diferencia
de percepción de colores entre uno y otro, sería una composición
de elementos con figuras parecidas a un árbol, un río y una montaña.
Pero los ojos de Bazi vieron un mundo y sus personajes.
