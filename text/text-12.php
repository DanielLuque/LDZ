Ado sacó de su mochila un cuaderno y un pedazo de carbón.
<br><br>
Tomó de la mano a Bazi y se la llevó a una fuente arábica rota que purificó en
algún momento la plaza, la cual daba de beber a las golondrinas,
a las alondras y las palomas que alrededor de ella crecían. Se sentó en la
esquina y empezó a dibujar.
